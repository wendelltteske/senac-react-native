var x = 20
var y = 10

/* Tipos de operações matemáticas: */

// adição +
console.log(`${x} + ${y} = ${x + y}`)

// subtração -
console.log(`${x} - ${y} = ${x - y}`)

// multiplicação *
console.log(`${x} * ${y} = ${x * y}`)

// divisão /
console.log(`${x} / ${y} = ${x / y}`)

// resto %
console.log(`${x} % ${y} = ${x % y}`)

/* Operadores unários: */

// incremento ++
var w = 10
w++ //adiciona um ao seu operando
console.log(w)

// decremento --
var z = 20
z-- //subtrai um ao seu operando
console.log(z)