var algarismo1 = 10
var algarismo2 = 20

/* Tipos de Comparativos: */

// maior que >
console.log(algarismo2 > algarismo1)

// menor que <
console.log(algarismo1 < algarismo2)

// maior ou igual a >=
console.log(algarismo2 >= algarismo1)

// menor ou igual a <=
console.log(algarismo1 <= algarismo2)

// igual a == (somente compara valor)
console.log(algarismo1 == algarismo2)

// diferente de !=
console.log(algarismo1 != algarismo2)

// idêntico a === (compara valor e tipo)
console.log(algarismo1 === algarismo2)

// não idêntico a !==
console.log(algarismo1 !== algarismo2)

// e (sentido de adicionar) &&
console.log(algarismo1 && algarismo2)

// ou (sentido de opção) ||
console.log(algarismo1 || algarismo2)