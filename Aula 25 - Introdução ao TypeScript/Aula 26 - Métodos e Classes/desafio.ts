function somar_(n1:number, n2:number):number{
    return n1 + n2
}

function subtrair_(n1:number, n2:number):number{
    return n1 - n2
}

function multiplicar_(n1:number, n2:number):number{
    return n1 * n2
}

function dividir_(n1:number, n2:number):number{
    return n1 / n2
}

export {somar_, subtrair_, multiplicar_, dividir_}